// site.js (debajo del init anterior)
(function () {
  // Helpers
  const addWow = (sel, anim, delay = 0, duration = .7) => {
    document.querySelectorAll(sel).forEach((el, i) => {
      // evita duplicados
      if (!el.classList.contains('wow')) el.classList.add('wow');
      el.classList.add('animate__' + anim);
      el.setAttribute('data-wow-duration', duration + 's');

      // escalonado para elementos en grupo
      const d = (typeof delay === 'function') ? delay(i, el) : delay;
      el.setAttribute('data-wow-delay', (typeof d === 'number' ? d : 0) + 's');
    });
  };

  // Hero (títulos, subtítulos, badges/botones)
  addWow('.hero-title',        'fadeInUp', 0.00);
  addWow('.hero-sub',          'fadeInUp', 0.10);
  addWow('.hero-badges .badge','fadeIn',   (i)=>0.20 + i*0.10);
  addWow('.hero-content .btn', 'fadeInUp', (i)=>0.20 + i*0.10);

  // Encabezados de sección
  addWow('.section-head h2',   'fadeInUp', 0.00);
  addWow('.section-head p',    'fadeInUp', 0.05);

  // Cards de “Servicios principales”
  addWow('.svc-card',          'fadeInUp', (i)=>0.00 + (i%3)*0.10);

  // Secciones alternadas (tu layout actual)
  addWow('.psc-altrow__copy',  'fadeInUp', 0.00);
  addWow('.psc-altrow__media', 'fadeInUp', 0.10);

  // Formulario y mapa de contacto
  addWow('#contacto .contact-form', 'fadeInUp', 0.00);
  addWow('#contacto .map-card',     'fadeInUp', 0.10);

  // Carrusel de logos / marcas (contenedor y cada logo)
  addWow('.brands-section .section-title', 'fadeInUp', 0.00);
  addWow('.brands-swiper',                 'fadeInUp', 0.05);
  addWow('.brands-swiper .brand-logo',     'fadeIn',   (i)=>0.10 + (i%6)*0.05);

  // Footer (tus 4 columnas)
  addWow('.psc-footer .col-lg-3:nth-child(1)', 'fadeInUp', 0.00);
  addWow('.psc-footer .col-lg-3:nth-child(2)', 'fadeInUp', 0.05);
  addWow('.psc-footer .col-lg-3:nth-child(3)', 'fadeInUp', 0.10);
  addWow('.psc-footer .col-lg-3:nth-child(4)', 'fadeInUp', 0.15);
})();

